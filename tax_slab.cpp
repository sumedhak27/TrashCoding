#include <iostream>
typedef long long ll;
using namespace std;

int main() {
    int t;
    cin>>t;
    while(t--) {
        ll n;
        cin>>n;
        ll tax = 0;
        if(n <= 250000) tax = 0;
        else if(n > 250000 && n <= 500000) {
            tax = (n-250000)*0.05;
        }
        else if(n > 500000 && n <= 750000) {
            tax = (500000-250000)*0.05 + (n-500000)*0.1;
        }
        else if(n> 750000 && n <= 1000000) {
            tax = (500000-250000)*0.05 +(750000-500000)*0.1 + (n - 750000)*0.15;
        }
        else if(n> 1000000 && n <= 1250000) {
            tax = (500000-250000)*0.05 +(750000-500000)*0.1 + (1000000 - 750000)*0.15  + (n-1000000)*0.20;
        }
        else if(n> 1250000 && n <= 1500000) {
            tax = (500000-250000)*0.05 +(750000-500000)*0.1 + (1000000 - 750000)*0.15  + (1250000-1000000)*0.20 + (n-1250000)*0.25;
        }
        else {
            tax = (500000-250000)*0.05 +(750000-500000)*0.1 + (1000000 - 750000)*0.15  + (1250000-1000000)*0.20 + (1500000-1250000)*0.25 + (n - 1500000)*0.30;

        }
        cout<<n-tax<<endl;
    }
    return 0;
}